#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	dummy_int;

	dummy_int = *a;
	*a = *b;
	*b = dummy_int;
}

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (i < size - 1)
	{
		j = 0;
		while (j < size - 1)
		{
			if (tab[j] > tab[j + 1])
				ft_swap(&tab[j], &tab[j + 1]);
			j++;
		}
		i++;
	}
}
