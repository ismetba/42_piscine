int	ft_is_prime(int nb)
{
	long	i;

	i = 2;
	if ((long)nb < 2)
		return (0);
	while (i * i <= (long)nb)
	{
		if (!((long)nb % i))
		{
			return (0);
		}
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	while (1)
	{
		if (ft_is_prime(nb))
			return (nb);
		nb++;
	}
}
